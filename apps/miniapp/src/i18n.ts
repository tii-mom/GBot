export function detectLocale(input?: string | null): Locale {
  const normalized = (input || "").toLowerCase();
  if (normalized.startsWith("zh")) return "zh-CN";
  if (normalized.startsWith("ko")) return "ko";
  return "en";
}

export function getLocaleLabel(locale: Locale): string {
  if (locale === "zh-CN") return "中文";
  if (locale === "ko") return "한국어";
  return "EN";
}

export function createTranslator(locale: Locale) {
  const dict = dictionaries[locale] || dictionaries.en;
  return (key: string, fallback: string) => dict[key] || fallback;
}

export type Translator = ReturnType<typeof createTranslator>;

export function translateRarity(t: Translator, rarity?: string): string {
  return t(`common.${rarity || "common"}`, rarity || "common");
}

